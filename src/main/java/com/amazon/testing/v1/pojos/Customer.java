package com.amazon.testing.v1.pojos;

public class Customer {

	public int id;
	public String ap_customer_name;
	public String ap_email;
	public String ap_phone_number;
	public String ap_password;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getAp_customer_name() {
		return ap_customer_name;
	}

	public void setAp_customer_name(String ap_customer_name) {
		this.ap_customer_name = ap_customer_name;
	}

	public String getAp_email() {
		return ap_email;
	}

	public void setAp_email(String ap_email) {
		this.ap_email = ap_email;
	}

	public String getAp_phone_number() {
		return ap_phone_number;
	}

	public void setAp_phone_number(String ap_phone_number) {
		this.ap_phone_number = ap_phone_number;
	}

	public String getAp_password() {
		return ap_password;
	}

	public void setAp_password(String ap_password) {
		this.ap_password = ap_password;
	}
}
